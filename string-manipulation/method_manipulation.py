# you can also manipulate string using python in-app method

myString = "This is a string"  # of course it is
print(len(myString))  # how long

print(myString.lower())  # lowercases it all
print(myString.swapcase())  # swap the cases?

print(myString.split(" "))  # you can split it to list

print(list(myString.replace(" ", "")))  # or make it a list?
